public class IterationControlStatement {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) { // for loop
            System.out.println("Bangladesh. " + i);
        }

        int j = 2;
        while (j <= 10) {
            System.out.println(j + ". July Freedom.");
            j += 2;
        }

        int k = 35;
        do {
            System.out.println(k + " July Evolution");
            k++;
        } while (k <= 35);
    }
}
